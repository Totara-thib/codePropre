package ex1;

/**
 * Classe Calcul qui effectue des méthodes de calcul
 * 
 * @author Thibaud
 */

public class Calcul {

	/**
	 * Méthode addition qui retourne l'addition entre les 2 entier 'a' et 'b'
	 * fournis en parametres
	 * 
	 * @param a est un entier
	 * @param b est un entier
	 * @return le résultat de l'addition entre 'a' et 'b'
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode substract qui retourne la soustraction entre les 2 entier 'a' et 'b'
	 * fournis en parametres
	 * 
	 * @param a est un entier
	 * @param b est un entier
	 * @return le résultat de la soustraction entre 'a' et 'b'
	 */
	public int substract(int a, int b){
		return a-b;
	}
}
